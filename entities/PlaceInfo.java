package entities;

public class PlaceInfo {
    private int placeID;
    private int productID;
    private int height_cm;
    private int width_cm;
    private int length_cm;

    public int getPlaceID() {
        return placeID;
    }

    public void setPlaceID(int placeID) {
        this.placeID = placeID;
    }

    public int getProductID() {
        return productID;
    }

    public void setProductID(int productID) {
        this.productID = productID;
    }

    public int getHeight_cm() {
        return height_cm;
    }

    public void setHeight_cm(int height_cm) {
        this.height_cm = height_cm;
    }

    public int getWidth_cm() {
        return width_cm;
    }

    public void setWidth_cm(int width_cm) {
        this.width_cm = width_cm;
    }

    public int getLength_cm() {
        return length_cm;
    }

    public void setLength_cm(int length_cm) {
        this.length_cm = length_cm;
    }
}
